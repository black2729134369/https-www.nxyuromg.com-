<script>
  var url = window.location.href; //获取当前页面的URL地址
  var params = url.split('?')[1]; //以问号为分隔符将URL分成两份，取第二份
  var paramObj = {}; //声明一个空对象，用于保存参数
  if (params) {
    var paramArr = params.split('&'); //以&为分隔符将参数分割成数组
    for (var i = 0; i < paramArr.length; i++) {
      var keyValue = paramArr[i].split('='); //以=为分隔符将键值对分割成数组
      var key = keyValue[0]; //取键
      var value = keyValue[1]; //取值
      paramObj[key] = value; //将键值对存入对象
    }
  }
  const batchNumber = paramObj.params;
  // 将批次号绑定到 input
  const inputElement = document.getElementById('inputElement');
  if (inputElement && batchNumber) {
    inputElement.value = decodeURIComponent(batchNumber); // 设置 input 的值
  }
  // 绑定按钮点击事件
  const submitButton = document.getElementById('submit');
  if (submitButton) {
    submitButton.addEventListener('click', function (event) {
      event.preventDefault(); // 阻止默认表单提交行为
      // alert('点击了按钮'); // 提示用户点击了按钮
      const inputValue = inputElement.value; // 获取输入框的值
      var Pid = inputValue.split('030405')[0]; 
      const url = '/index.php/productDetail_57/' + Pid + '.html' 
      if (inputValue) {
        window.location.href = url; // 跳转到目标页面
      } else {
        alert('请输入批次号'); // 提示用户输入批次号
      }
    });
  }
  console.log("批次号", batchNumber); //输出参数对象
</script>